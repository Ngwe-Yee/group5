package com.napier.sem;
/**
 * Represents an City
 */
public class City {
    /**
     * City ID
     */
    public int ID;

    /**
     * City name
     */
    public String Name;

    /**
     * City CountryCode
     */
    public String CountryCode;

    /**
     * City District
     */
    public String District;

    /**
     * City Population
     */
    public int Population;

}
